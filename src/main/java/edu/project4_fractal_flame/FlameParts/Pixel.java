package edu.project4_fractal_flame.FlameParts;

// Внешний вид пикселя
// Класс Pixel для окрашивания пикселя
// Класс для хранения информации о пикселе
public class Pixel {
    private int r;
    private int g;
    private int b;

    public Pixel(int r, int g, int b) {
        this.r = r;
        this.g = g;
        this.b = b;
    }
}
